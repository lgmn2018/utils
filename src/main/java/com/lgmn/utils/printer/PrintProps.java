package com.lgmn.utils.printer;

public class PrintProps {
    private int x = 0;
    private int y = 0;
    private int width = 100;
    private int height = 100;
    private int gap_m = 4;
    private int gap_n = 2;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getGap_m() {
        return gap_m;
    }

    public void setGap_m(int gap_m) {
        this.gap_m = gap_m;
    }

    public int getGap_n() {
        return gap_n;
    }

    public void setGap_n(int gap_n) {
        this.gap_n = gap_n;
    }
}